    <p class="titleA">Wheat Cents</p>

    <p class="textA">
    The Silver Stand readily promotes and sells silver but certainly has a soft spot for wheat cents.
    <br>
    <br>
    Wheat cents are a fun and economical way to get started in coin collecting and stacking, especially for young kids. Parents can keep their kids occupied for a time as well as teach them some basic fundamentals about money, uniqueness and rarity. 
    <br>
    <br>
    Wheat cents were minted from 1909 to 1958 by the Philadelphia, San Francisco and Denver mints. Cents that do not have a mint mark were minted in Philadelphia, cents with an “S” mint mark were minted in San Francisco and cents with a “D” were minted in Denver.  
    <br>
    <br>
    The 1909 cent has a “VDB” variant that is very, very small at the bottom of the rim – and the “VDB”  with the “S” mint mark is a really rare find!
    <br>
    <br>
    In 1943, World War II caused a shortage of copper so cents were minted out of steel coated with zinc. Although not to rare, they are a nice novelty and due feel and sound very different!
    <br>
    <br>
    Although not as profound as 90% Constitutional Silver, pennies also have been debased, too. All wheat cents from 1909  to 1958  (with the exception being 1943 cents) and Lincoln memorial cents until 1981 are 95% copper and 5% tin/zinc. Then in 1982, they consist of 97.5% zinc and just 2.5% copper.
